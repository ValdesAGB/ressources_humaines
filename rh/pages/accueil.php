    <h1>GRH<span class='text-danger'>Bern</span> ?</h1>
   <p >
    Est un espace qui met en relation les employés,
    les candidats et l'entreprise
    La gestion des ressources humaines peut être définie comme la gestion des salariés dans une organisation et représente aujourd’hui une activité primordiale  dans l’entreprise assurant un ensemble de taches permettant d’assurer les différents services internes en matière de gestion de ressources humaines. Aujourd’hui  La GRH ne se contente
     plus de gérer les ressources humaines, mais contribue au développement du capital humain de l’entreprise, par la gestion des 6 activités qui interpellent ce dernier : gestion de recrutement, formation continue, rémunération, carrière, sanction, et communication interne. En vue  d’obtenir une adéquation efficace et durable entre les salariés de l’entreprise et les emplois, en termes d’effectifs, de qualification et de motivation. Mais avant d’en arriver a ce stade de maturité la fonction RH a évolué au fur et a mesure du temps. Avant on parlait de l’administration du personnel qui consistait à considérer le salarié comme étant un cout, et du coup elle se contentait d’administrer son recrutement, sa carrière, sa formation, sa paie en effectuant un ensemble de taches généralement administratif (rédaction du contrat du travail , pointages décompte des heures , déclaration fiscale…)  alors que maintenant c’est la gestion du personnel qui considère le salarié comme étant une ressource indispensable, un facteur de compétitivité qu’il faut gérer. C'est-à-dire gérer son recrutement,  
    sa formation continue, sa rémunération, sa carrière, la sanction ainsi que la communication interne.
